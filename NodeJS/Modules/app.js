//~ Modules --> It is a reusable piece of code.
//? why --> readable, manageable (updates, bug), scalable, encapsulation(hiding different functions and variables outside the modules)
//? DRY (don't repeat yourself)

//~ different module systems ->
//? 1) commonJS : used by nodeJS by default, in this we can export and import the modules by using following keywords : module.exports and require method (this is older version)

//? 2) ES modules : this is the newer version, i this we can export and import modules byt using following keywords: import and export

//! types of modules -> 3
//~ 1) local/user defined modules: these modules are written by user and are made available for import and export.

//~ 2) built-in modules: also called as core-modules, these are part of the installation. examples --> fs, http, os, crypto, qs, buffers, etc...

//~ 3) third party modules: here modules are installed from a third party which is called npm (node-package-manager). example --> express.js
